namespace BHS.Constants;

/// <summary>
/// Набор глобальных игровых констант, используемых в симуляции.
/// </summary>
/// <remarks>
/// <para>
/// Класс <see cref="GameConstants"/> содержит базовые значения,
/// определяющие скорость обновления игрового цикла и шаг времени симуляции.
/// </para>
/// <para>
/// Эти параметры используются всеми системами ECS для расчёта перемещения и таймингов.
/// </para>
/// </remarks>
public sealed class GameConstants
{
    /// <summary>
    /// Фиксированное значение временного шага, используемое при обновлении систем (в секундах).
    /// </summary>
    public const float DeltaTime = 0.16f;

    /// <summary>
    /// Интервал задержки между итерациями игрового цикла (в миллисекундах).
    /// </summary>
    public const int SleepTime = 16;
}